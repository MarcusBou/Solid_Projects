using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Help_Jakob
{
    /// <summary>
    /// MessageConverter is only for converting message
    /// </summary>
    public class MessageConverter
    {
        /// <summary>
        /// converts inserted plaintext to Html
        /// </summary>
        /// <param name="plainText"></param>
        /// <returns></returns>
        public string ConvertBodyToHTML(string plainText)
        {
            return "" + plainText + "";
        }
    }
}
